<template>
  <KeepAlive>
    <component
      v-if="containerType"
      :is="containerType"
      :container="container"
      :index="index"
      :length="length"
      :preview="preview" />
  </KeepAlive>
</template>
<script>
export default {
  props: {
    container: {
      type: Object,
      default: null,
    },
    index: {
      type: Number,
      default: null,
    },
    length: {
      type: Number,
      default: null,
    },
    preview: {
      type: Boolean,
      default: false,
    },
  },
  computed: {
    containerType() {
      const extension = this.container && this.$root.containerTypes.find(ext => ext?.isValid?.(this.container));
      return extension?.containerType;
    },
    params() {
      return {
        container: this.container,
      };
    },
  },
};
</script>